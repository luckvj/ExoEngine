// Helper function to get synergies by class
export function getSynergiesByClass(
  guardianClass: GuardianClass,
  ownedExpansions?: Expansion[]
): SynergyDefinition[] {
  return SYNERGY_DEFINITIONS.filter(
    (s) =>
      s.guardianClass === guardianClass &&
      (!s.requiredExpansion || !ownedExpansions || ownedExpansions.includes(s.requiredExpansion))
  );
}

// Helper function to get synergies by element
export function getSynergiesByElement(element: ElementType): SynergyDefinition[] {
  return SYNERGY_DEFINITIONS.filter((s) => s.element === element);
}

// Helper function to get beginner-friendly synergies
export function getBeginnerSynergies(): SynergyDefinition[] {
  return SYNERGY_DEFINITIONS.filter((s) => s.difficulty === Difficulty.Beginner);
}

// Helper function to search synergies by tag
export function getSynergiesByTag(tag: string): SynergyDefinition[] {
  return SYNERGY_DEFINITIONS.filter((s) =>
    s.tags.some((t) => t.toLowerCase().includes(tag.toLowerCase()))
  );
}
